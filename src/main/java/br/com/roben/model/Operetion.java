package br.com.roben.model;

public class Operetion {


    public Double soma(double x, double y) {

        return x + y;
    };


    public Double subtracao(double x, double y) {

        return x + y;
    };


    public Double mutiplicacao(double x, double y) {

        return x * y;
    };

    public Double  divisao(double x, double y){

        return  x/y;
    };


    public Double  media(double x, double y){

        return  (x+y)/2;
    };


    public Double  raizQuadrada(double x){

        return   Math.sqrt(x);
    };







}